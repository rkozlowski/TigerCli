          overflow-x: auto;
          font-family: "Cascadia Mono", Consolas, "DejaVu Sans Mono", monospace;
          font-size: 13px;
        }

        /* Stable HtmlSink decoration/link-role classes. */
        .tc-bold { font-weight: 700; }
        .tc-italic { font-style: italic; }
        .tc-underline { text-decoration: underline; }
        .tc-link { text-decoration: underline; }
        pre.tigercli a { color: inherit; }
        """;

    /// <summary>Builds a full example page. All shell text is HTML-escaped; fragment HTML is
    /// inserted verbatim (it is already escaped by <c>HtmlSink</c>).</summary>
    public static string BuildPage(string title, string intro, IReadOnlyList<Section> sections)
    {
        var sb = new StringBuilder();
        sb.Append("<!DOCTYPE html>\n");
        sb.Append("<html lang=\"en\">\n<head>\n");
        sb.Append("<meta charset=\"utf-8\">\n");
        sb.Append("<meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">\n");
        sb.Append("<title>").Append(Escape(title)).Append(" — TigerCli examples</title>\n");
        sb.Append("<link rel=\"stylesheet\" href=\"assets/tigercli.css\">\n");
        sb.Append("</head>\n<body>\n<main>\n");
        sb.Append("<h1>").Append(Escape(title)).Append("</h1>\n");
        sb.Append("<p class=\"intro\">").Append(Escape(intro)).Append("</p>\n");

        foreach (var section in sections)
        {
            sb.Append("<section>\n");
            sb.Append("<h2>").Append(Escape(section.Heading)).Append("</h2>\n");
            sb.Append("<p>").Append(Escape(section.Description)).Append("</p>\n");
            if (section.Source is not null)
                sb.Append("<pre class=\"tc-source\">").Append(Escape(section.Source)).Append("</pre>\n");
            sb.Append(section.FragmentHtml).Append('\n');
            sb.Append("</section>\n");
        }

        sb.Append("<footer>Generated by internal/DocSamples from real TigerCli rendering. ");
        sb.Append("Do not edit by hand — regenerate with: dotnet run --project internal/DocSamples</footer>\n");
        sb.Append("</main>\n</body>\n</html>\n");
        return sb.ToString();
    }

    private static string Escape(string text) => text
        .Replace("&", "&amp;")
        .Replace("<", "&lt;")
        .Replace(">", "&gt;");
}
